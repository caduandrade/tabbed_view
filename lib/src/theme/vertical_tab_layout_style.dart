/// Defines the layout style for a tab in a vertical [TabBar].
enum VerticalTabLayoutStyle {
  /// The tab's internal components (leading, text, and buttons) are arranged
  /// in a row. Text is rotated vertically.
  inline,

  /// The tab's internal components (leading, text, and buttons) are arranged
  /// in a column. Text is not rotated.
  stacked
}
